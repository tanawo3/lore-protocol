    def _require_owner(self):
        if str(gl.message.sender_address) != self.owner:
            raise gl.vm.UserError(f"{ERROR_EXPECTED} Unauthorized: Not the owner")

    @gl.public.write.payable
    def submit_proposal(self, proposal_id: str, borrower: str, requested_amount: int, text_proposal: str) -> bool:
        """
        Ingests a new lore entry from an external client.
        
        Args:
            proposal_id: External UUID string.
            borrower: Identifier string (creator).
            requested_amount: Integer representing reputation points.
            text_proposal: Unstructured text detailing the lore addition.
            
        Returns:
            bool: True if ingestion was successful.
            
        Raises:
            gl.vm.UserError: If inputs violate deterministic invariants.
        """
        if proposal_id in self.proposals:
            raise gl.vm.UserError(f"{ERROR_EXPECTED} Entry already exists")
            
        if requested_amount <= 0:
            raise gl.vm.UserError(f"{ERROR_EXPECTED} Amount must be > 0")
        
        # Rigorous input sanitization against prompt injection attacks
        clean_creator = _deep_sanitize(borrower)[:128]
        clean_text = _deep_sanitize(text_proposal)[:MAX_LORE_LEN]
        
        entry = LoreEntry(
            proposal_id=proposal_id,
            creator=str(gl.message.sender_address),
            reputation_requested=u256(requested_amount),
            lore_text=clean_text,
            status="PENDING",
            ai_reasoning="",
            risk_score=u256(0),
            collateral=u256(gl.message.value),
            debt=u256(0),
            created_at=u256(0), # Epoch 0
            last_updated=u256(0)
        )
        self.proposals[proposal_id] = entry
        self.proposal_ids.append(proposal_id)
        
        self.state.total_reputation_requested = u256(int(self.state.total_reputation_requested) + requested_amount)
        return True

    @gl.public.write
    def evaluate_proposal(self, proposal_id: str) -> bool:
        """
        Triggers the GenLayer Subjective Consensus engine to curate the lore.
        
        This method utilizes a Leader-Validator multi-agent protocol. 
        The Leader executes a complex LLM prompt to determine narrative fit.
        The Validators independently critique the Leader's reasoning and ensure
        it satisfies the required logical constraints before persisting to state.
        
        Args:
            proposal_id (str): Target proposal ID to curate.
            
        Returns:
            bool: True if consensus was reached and state was updated.
        """
        self._require_owner()
        if proposal_id not in self.proposals:
            raise gl.vm.UserError(f"{ERROR_EXPECTED} Entry not found")
            
        entry = self.proposals[proposal_id]
        if entry.status != "PENDING":
            raise gl.vm.UserError(f"{ERROR_EXPECTED} Not pending")
            
        # Extract to local variables to prevent state access inside nondet block
        lore_text = entry.lore_text
        current_world_lore = self.world_lore
        amount = int(entry.reputation_requested)
        
        def leader_fn() -> dict:
            """Leader execution environment."""
            
            url = _extract_url(lore_text)
            if not url: return {"verdict": "REJECTED", "risk_score": 10000, "summary": "No external URL provided."}
            try:
                body = gl.nondet.web.get(url, mode="text") or ""
                lower_body = body.lower()
                for marker in ["cloudflare", "ddos protection", "are you human", "captcha"]:
                    if marker in lower_body:
                        raise Exception(f"challenge page detected: {marker}")
                canon_data = body[:1000]
            except Exception:
                canon_data = "NO EXTERNAL CANON DATA."
                
            prompt = _interpret_leader_prompt(current_world_lore, lore_text, canon_data)
            analysis = gl.nondet.exec_prompt(prompt, response_format="json")
            return {
                "verdict": _parse_verdict(analysis),
                "risk_score": _parse_ratio_bps(analysis),
                "summary": _clean_summary(analysis)
            }
            
        # Execute Subjective Consensus Engine
        decision = gl.eq_principle.prompt_comparative(
            leader_fn,
            principle="The AI verdicts must match and the risk score BPS should be within 10% of each other. The summaries must carry logically equivalent semantic reasoning."
        )
        
        # State transitions based on consensus outcome
        entry.status = decision["verdict"]
        entry.ai_reasoning = decision["summary"]
        entry.risk_score = u256(decision["risk_score"])
        entry.last_updated = u256(int(entry.last_updated) + 1)
        if entry.status == "APPROVED":
            loan_amount = int(entry.reputation_requested)
            entry.debt = u256(loan_amount + loan_amount // 20)  # 5% interest
            if loan_amount > 0:
                _NativeRecipient(Address(entry.creator)).emit_transfer(value=u256(loan_amount))
        self.proposals[proposal_id] = entry
        
        # Update System Metrics
        self.state.total_processed = u256(int(self.state.total_processed) + 1)
        if entry.status == "APPROVED":
            self.state.total_approved = u256(int(self.state.total_approved) + 1)
            self.state.total_reputation_approved = u256(int(self.state.total_reputation_approved) + amount)
            self.world_lore += f" Then, {lore_text}"
        else:
            self.state.total_rejected = u256(int(self.state.total_rejected) + 1)
            
        # Recalculate global coherence index
        self._recalculate_global_coherence()
        return True


    @gl.public.write.payable
    def repay_loan(self, proposal_id: str) -> bool:
        """
        Allows a creator to repay their debt and reclaim their collateral.
        Enforces strict authorization and value checking.
        """
        if proposal_id not in self.proposals:
            raise gl.vm.UserError(f"{ERROR_EXPECTED} Entry not found")
        entry = self.proposals[proposal_id]
        if str(gl.message.sender_address) != entry.creator:
            raise gl.vm.UserError(f"{ERROR_EXPECTED} Unauthorized: Not the creator")
        if entry.status != "APPROVED":
            raise gl.vm.UserError(f"{ERROR_EXPECTED} Loan is not in active state")
        debt = int(entry.debt)
        if gl.message.value < debt:
            raise gl.vm.UserError(f"{ERROR_EXPECTED} Insufficient repayment. Need {debt}")
        collateral_amount = int(entry.collateral)
        if collateral_amount > 0:
            _NativeRecipient(Address(entry.creator)).emit_transfer(value=u256(collateral_amount))
        entry.status = "REPAID"
        entry.debt = u256(0)
        entry.last_updated = u256(int(entry.last_updated) + 1)
        self.proposals[proposal_id] = entry
        return True

    @gl.public.write
    def revoke_proposal(self, proposal_id: str) -> bool:
        """
        Allows an external system or governance module to forcibly revoke 
        a proposal that was maliciously submitted or violated TOS.
        """
        if proposal_id not in self.proposals:
            raise gl.vm.UserError(f"{ERROR_EXPECTED} Entry not found")
            
        entry = self.proposals[proposal_id]
        if str(gl.message.sender_address) != entry.creator:
            raise gl.vm.UserError(f"{ERROR_EXPECTED} Unauthorized: Not the creator")
        if entry.status == "REVOKED":
            return True # Idempotent
        if entry.status == "PENDING":
            collateral_amount = int(entry.collateral)
            if collateral_amount > 0:
                _NativeRecipient(Address(entry.creator)).emit_transfer(value=u256(collateral_amount))
        old_status = entry.status
        entry.status = "REVOKED"
        entry.ai_reasoning = "Revoked by creator."
        entry.last_updated = u256(int(entry.last_updated) + 1)
        self.proposals[proposal_id] = entry
        
        self.state.total_revoked = u256(int(self.state.total_revoked) + 1)
        
        if old_status == "APPROVED":
            self.state.total_approved = u256(int(self.state.total_approved) - 1)
            self.state.total_reputation_approved = u256(int(self.state.total_reputation_approved) - int(entry.reputation_requested))
            self._recalculate_global_coherence()
            
        return True

