    def _recalculate_global_coherence(self):
        """
        Iterates over recent approved entries to calculate a rolling 
        weighted average of the systemic narrative coherence.
        """
        total_weight = 0
        total_risk = 0
        count = 0
        
        # Iterate backwards over the array
        idx = len(self.proposal_ids) - 1
        while idx >= 0 and count < MAX_SYSTEM_METRICS:
            pid = self.proposal_ids[idx]
            p = self.proposals[pid]
            if p.status == "APPROVED":
                amt = int(p.reputation_requested)
                r_score = int(p.risk_score)
                total_weight += amt
                total_risk += (amt * r_score)
                count += 1
            idx -= 1
            
        if total_weight > 0:
            self.state.global_coherence_index_bps = u256(total_risk // total_weight)
        else:
            self.state.global_coherence_index_bps = u256(0)


